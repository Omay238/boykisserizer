let images = {};
let boykisser;
let imageInput;

function preload() {
    for (let i in json) {
        images[i] = loadImage('flags/' + i + '.png');
        document.querySelector("#bar").width = Math.round(images.length / json.length) + "%";
        document.querySelector("#a").innerText = images.length;
        document.querySelector("#b").innerText = json.length;
    }
    boykisser = loadImage('boykissertr.png');
}

function setup() {
    createCanvas(boykisser.width / 2, boykisser.height / 2);
    imageMode(CENTER);
    for (let i in json) {
        let option = document.createElement('option');
        option.value = i;
        option.innerHTML = json[i];
        document.querySelector('#flags').appendChild(option);
    }
    document.querySelector("#loader").remove();

    imageInput = select("#imageInput")
    imageInput.changed(handleImage);
}

function handleImage() {
  const file = imageInput.elt.files[0];

  if (file) {
    const reader = new FileReader();

    reader.onload = function (event) {
      const img = createImg(event.target.result, 'uploaded image', '', () => {
        let aspectRatio = img.width / img.height;
        let dWidth = height * aspectRatio;
        image(img, width / 2, height / 2, dWidth, height);
        image(boykisser, width / 2, height / 2, width, height);
      });
      img.hide();
    };

    reader.readAsDataURL(file);
  }
}

function loadImage() {
  imageInput.elt.click();
}

document.querySelector("#generate").addEventListener("click", () => {
    let img = images[document.querySelector("#flags").value];
    let aspectRatio = img.width / img.height;
    let dWidth = height * aspectRatio;
    image(img, width / 2, height / 2, dWidth, height);
    image(boykisser, width / 2, height / 2, width, height);
});
